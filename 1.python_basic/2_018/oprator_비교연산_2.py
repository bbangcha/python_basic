userInputNum1 = int(input('첫번째 숫자 입력 : '))
userInputNum2 = int(input('두번째 숫자 입력 : '))

print('{} > {} : {}'.format(userInputNum1, userInputNum2, (userInputNum1 > userInputNum2)))
print('{} >= {} : {}'.format(userInputNum1, userInputNum2, (userInputNum1 >= userInputNum2)))
print('{} < {} : {}'.format(userInputNum1, userInputNum2, (userInputNum1 < userInputNum2)))
print('{} <= {} : {}'.format(userInputNum1, userInputNum2, (userInputNum1 <= userInputNum2)))
print('{} == {} : {}'.format(userInputNum1, userInputNum2, (userInputNum1 == userInputNum2)))
print('{} != {} : {}'.format(userInputNum1, userInputNum2, (userInputNum1 != userInputNum2)))