seasonStr = input('계절 입력 : ')
if seasonStr == '봄':
    print('{}, {}'.format('봄', 'spring'))
elif seasonStr == '여름':
    print('{}, {}'.format('여름', 'summer'))
elif seasonStr == '가을':
    print('{}, {}'.format('가을', 'fall'))
elif seasonStr == '겨울':
    print('{}, {}'.format('겨울', 'winter'))
else:
    print('검색할 수 없습니다.')
    



